package bean;

public class CodigoAplicacao {

	private int codAplicacao;
	private int fixo;
	private String nome;
	private int codigoGeral;
	private String codigo;

	public int getCodigoGeral() {
		return codigoGeral;
	}

	public void setCodigoGeral(int codigoGeral) {
		this.codigoGeral = codigoGeral;
	}

	public int getCodAplicacao() {
		return codAplicacao;
	}

	public void setCodAplicacao(int codAplicacao) {
		this.codAplicacao = codAplicacao;
	}

	public int getFixo() {
		return fixo;
	}

	public void setFixo(int fixo) {
		this.fixo = fixo;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getCodigo() {
		return codigo;
	}

	public void setCodigo(String codigo) {
		this.codigo = codigo;
	}
	 
	

}
